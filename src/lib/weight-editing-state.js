/**
 * Глобальное состояние "сейчас редактируется вес в какой-то карточке".
 *
 * Зачем нужен этот модуль:
 *
 * Когда юзер тапает на поле веса в карточке упражнения — открывается
 * системная клавиатура iOS. Если в этот момент он тапает по СОСЕДНЕЙ карточке:
 *  - iOS закрывает клавиатуру (это его родное поведение)
 *  - срабатывает onClick соседней карточки → она "активируется" (помечается выполненной)
 *
 * Юзер этого не ожидает: он хотел просто закрыть клавиатуру тапом мимо, а получил
 * случайную активацию упражнения.
 *
 * Раньше защита была локальной (внутри одной карточки через useRef). Теперь делаем
 * глобально: пока хоть одна карточка редактирует вес, ВСЕ остальные карточки
 * "знают" об этом и не реагируют на тап. Они просто гасят тап (закрытие клавиатуры
 * iOS сделает сам, через onBlur инпута).
 *
 * Архитектура:
 *  - editingState — приватный объект с двумя флагами:
 *      isEditing — кто-то прямо сейчас вводит вес
 *      justClosedAt — таймстамп последнего закрытия клавиатуры (защита 300мс)
 *  - функции set/clear меняют состояние
 *  - shouldIgnoreCardTap() — единая точка где карточка спрашивает "мне игнорить тап?"
 *
 * Используется только в src/components/ExerciseCard.jsx — пока нет смысла
 * выносить в общий API.
 */

const editingState = {
  isEditing: false,
  justClosedAt: 0
}

/**
 * Сколько миллисекунд после закрытия клавиатуры карточки продолжают игнорировать тап.
 * Это страховка от ситуации когда iOS отправляет click ПОСЛЕ blur (так бывает на старых iOS).
 */
const COOLDOWN_MS = 300

/**
 * Вызывается из onFocus инпута веса — кто-то начал редактировать.
 */
export function markWeightEditingStarted() {
  editingState.isEditing = true
}

/**
 * Вызывается из onBlur инпута веса — редактирование закончилось.
 * Ставим таймстамп — последующие 300мс все карточки будут игнорировать тапы.
 */
export function markWeightEditingEnded() {
  editingState.isEditing = false
  editingState.justClosedAt = Date.now()
}

/**
 * Главный вопрос: должна ли карточка проигнорировать тап?
 * Возвращает true если:
 *  - где-то прямо сейчас редактируется вес
 *  - ИЛИ редактирование закончилось менее 300мс назад
 */
export function shouldIgnoreCardTap() {
  if (editingState.isEditing) return true
  if (Date.now() - editingState.justClosedAt < COOLDOWN_MS) return true
  return false
}